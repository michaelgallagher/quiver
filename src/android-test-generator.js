/**
 * Generates a Kotlin instrumented test file (QuiverCapture.kt) that navigates
 * to every reachable Compose screen in the parsed graph and writes a PNG
 * screenshot to the app's externalCacheDir.
 *
 * Strategy (different from iOS XCUITest):
 *  - Navigate programmatically via a TestHooks singleton that exposes the
 *    running NavHostController to instrumented tests. The prototype must have
 *    TestHooks + a LaunchedEffect hook in its AppNavigation.kt — the kotlin-crawler
 *    auto-injects these if missing.
 *  - Each @Test calls navController.navigate("<rawRoute>") directly, waits for
 *    idle, then uses composeTestRule.onRoot().captureToImage() to capture the
 *    current screen.
 *  - Routes with {placeholder} params are skipped unless an override with a
 *    concrete route is provided in config.overrides.
 */

/**
 * Sanitize a node ID for use as a filesystem filename / Kotlin identifier.
 * Replaces all non-alphanumeric characters with `_` and truncates to 200 chars.
 */
function sanitizeFilename(id) {
  return String(id).replace(/[^a-zA-Z0-9]/g, "_").slice(0, 200);
}

/**
 * Generate the full Kotlin test file content.
 *
 * @param {object} graph - { nodes, edges }
 * @param {string} packageName - Android app package (e.g. "com.prototype.nhsappnotab")
 * @param {string} mainActivityClass - fully-qualified class (e.g. "com.prototype.nhsappnotab.MainActivity")
 * @param {object} overrides - map of nodeId → { route: string }
 * @returns {string|null} Kotlin source, or null if no tests would be generated
 */
function generateAndroidTest(graph, packageName, mainActivityClass, overrides = {}) {
  const screenNodes = graph.nodes.filter((n) => n.type === "screen");

  const tests = [];
  const skipped = [];

  for (const node of screenNodes) {
    const override = overrides[node.id];

    // Override always wins
    if (override && typeof override.route === "string") {
      tests.push({ safeId: sanitizeFilename(node.id), route: override.route });
      continue;
    }

    // Phantom nodes (created by graph-builder when a navigate() targets a
    // canonical route with no matching NavHost entry) have no rawRoute.
    // We can't reliably navigate to them, so skip.
    if (!node.rawRoute) {
      skipped.push({ id: node.id, reason: "no NavHost entry" });
      continue;
    }

    // Parameterized routes: substitute each {placeholder} with a value from
    // override.params → declared defaultValue → type-aware fallback.
    if (/\{[^}]+\}/.test(node.rawRoute)) {
      const overrideParams = (override && override.params) || {};
      const { route: resolved, unresolved } = substitutePlaceholders(
        node.rawRoute,
        node.navArgs || [],
        overrideParams,
      );
      if (unresolved.length > 0) {
        skipped.push({
          id: node.id,
          reason: `unresolved params (${unresolved.join(", ")}) in ${node.rawRoute}`,
        });
        continue;
      }
      tests.push({ safeId: sanitizeFilename(node.id), route: resolved });
      continue;
    }

    tests.push({ safeId: sanitizeFilename(node.id), route: node.rawRoute });
  }

  if (tests.length === 0) return null;

  return renderTestFile({
    packageName,
    mainActivityClass,
    tests,
    skipped,
  });
}

function renderTestFile({ packageName, mainActivityClass, tests, skipped }) {
  const methods = tests.map(renderTestMethod).join("\n");
  const skipComments = skipped.length
    ? "\n    // Skipped (add to config overrides.<id>.route to capture):\n" +
      skipped.map((s) => `    //   ${s.id}  — ${s.reason}`).join("\n") +
      "\n"
    : "";

  return `package ${packageName}

// Auto-generated by quiver.
// This file is temporary — the tool writes it at run time and deletes it
// afterwards. Do not edit or commit.

import android.content.Context
import android.graphics.Bitmap
import android.util.Log
import androidx.compose.ui.graphics.asAndroidBitmap
import androidx.compose.ui.test.captureToImage
import androidx.compose.ui.test.junit4.createAndroidComposeRule
import androidx.compose.ui.test.onRoot
import androidx.test.ext.junit.runners.AndroidJUnit4
import androidx.test.platform.app.InstrumentationRegistry
import ${packageName}.navigation.TestHooks
import org.junit.Before
import org.junit.BeforeClass
import org.junit.Rule
import org.junit.Test
import org.junit.runner.RunWith
import java.io.File
import java.io.FileOutputStream

@RunWith(AndroidJUnit4::class)
class QuiverCapture {

    companion object {
        @JvmStatic
        @BeforeClass
        fun skipOnboarding() {
            val ctx = InstrumentationRegistry.getInstrumentation().targetContext
            ctx.getSharedPreferences("onboarding", Context.MODE_PRIVATE)
                .edit()
                .putBoolean("completed", true)
                .commit()
        }
    }

    @get:Rule
    val composeTestRule = createAndroidComposeRule<${mainActivityClass}>()

    @Before
    fun waitForNavController() {
        composeTestRule.waitUntil(timeoutMillis = 10_000) {
            TestHooks.navController != null
        }
    }

    private fun writeScreenshot(name: String) {
        val bitmap = composeTestRule.onRoot().captureToImage().asAndroidBitmap()
        val ctx = InstrumentationRegistry.getInstrumentation().targetContext
        val dir = File(ctx.externalCacheDir, "flow-map").apply { mkdirs() }
        val file = File(dir, "$name.png")
        FileOutputStream(file).use { bitmap.compress(Bitmap.CompressFormat.PNG, 100, it) }
        Log.d("QuiverCapture", "Wrote \${file.absolutePath} size=\${file.length()}")
    }

    private fun navigateAndCapture(route: String, name: String) {
        try {
            composeTestRule.runOnUiThread { TestHooks.navController!!.navigate(route) }
        } catch (t: Throwable) {
            Log.w("QuiverCapture", "navigate(\\"$route\\") threw: \${t.message}")
        }
        composeTestRule.waitForIdle()
        Thread.sleep(500)
        writeScreenshot(name)
    }
${skipComments}
    // --- Per-screen captures ---
${methods}
}
`;
}

function renderTestMethod({ safeId, route }) {
  const escapedRoute = kotlinEscape(route);
  return `
    @Test
    fun testCapture_${safeId}() {
        navigateAndCapture("${escapedRoute}", "${safeId}")
    }`;
}

/**
 * Substitute {placeholder} tokens in a route string with concrete values.
 *
 * Resolution order per placeholder name:
 *   1. overrideParams[name] — caller-supplied concrete value
 *   2. matching navArg.defaultValue declared in the composable() registration
 *   3. Type-aware fallback: StringType→"1", BoolType→"false", Int/Long→"0",
 *      Float→"0.0", anything else→"1"
 *
 * URL-encodes string values so multi-segment paths don't break the route.
 *
 * Returns { route, unresolved }. `unresolved` lists placeholders that had no
 * value at all — shouldn't normally happen since we always have a fallback,
 * but surfaces here if a navArg is malformed.
 */
function substitutePlaceholders(rawRoute, navArgs, overrideParams) {
  const byName = new Map(navArgs.map((a) => [a.name, a]));
  const unresolved = [];

  const route = rawRoute.replace(/\{([^}]+)\}/g, (_, name) => {
    if (Object.prototype.hasOwnProperty.call(overrideParams, name)) {
      return encodeForRoute(overrideParams[name]);
    }
    const arg = byName.get(name);
    // Ignore empty-string defaults: they're valid in Compose (the screen
    // applies `.ifBlank { ... }` fallbacks) but produce empty path segments
    // that fail to match the NavHost route pattern.
    if (arg && arg.defaultValue !== null && arg.defaultValue !== undefined && arg.defaultValue !== "") {
      return encodeForRoute(arg.defaultValue);
    }
    // Seed-data sample extracted from the ViewModel (e.g. "trusted-1") —
    // lets state-dependent screens render with real data instead of blanks.
    if (arg && arg.sampleValue) {
      return encodeForRoute(arg.sampleValue);
    }
    const fallback = fallbackForType(arg && arg.type);
    if (fallback === null) {
      unresolved.push(name);
      return `{${name}}`;
    }
    return encodeForRoute(fallback);
  });

  return { route, unresolved };
}

function fallbackForType(type) {
  switch (type) {
    case "BoolType":
      return "false";
    case "IntType":
    case "LongType":
      return "0";
    case "FloatType":
      return "0.0";
    case "StringType":
    case null:
    case undefined:
      return "1";
    default:
      return "1";
  }
}

function encodeForRoute(value) {
  const s = String(value);
  // Empty string is a valid value for some flows (e.g. additionalInfo default)
  // — but an empty path segment would break the route. Leave empty as-is when
  // Compose declares it; Kotlin side handles `.ifBlank { ... }` fallbacks.
  if (s === "") return "";
  return encodeURIComponent(s).replace(/%2F/g, "/");
}

function kotlinEscape(s) {
  return String(s)
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\$/g, "\\$")
    .replace(/\n/g, "\\n");
}

module.exports = { generateAndroidTest, sanitizeFilename };
